package es.uji.ei1027.oviaplication.model;

public class Inscription {
    private Integer idNumber;
    public String idovi;
    public String idpap;
    public String idext;
    public Integer idactivity;

    public Inscription() {}

    public Integer getIdNumber() { return idNumber; }
    public void setIdNumber(Integer idNumber) { this.idNumber = idNumber; }

    public String getIdovi() { return idovi; }
    public void setIdovi(String idovi) { this.idovi = idovi; }


    public String getIdpap() { return idpap; }
    public void setIdpap(String idpap) { this.idpap = idpap; }

    public String getIdext() { return idext; }
    public void setIdext(String idext) { this.idext = idext; }

    public Integer getIdactivity() { return idactivity; }
    public void setIdactivity(Integer idactivity) { this.idactivity = idactivity; }


}
